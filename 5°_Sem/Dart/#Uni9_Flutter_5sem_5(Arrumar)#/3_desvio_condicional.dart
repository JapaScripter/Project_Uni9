void main() {
  
  int idade = 18;
  
  if (idade >= 18) {
    print("A pessoa é maior de idade.");
  } else {
    print("A pessoa é menor de idade.");
  }
  
  bool ativo = true;
  
  if (ativo) {
    print("O usuário está ativo.");
  } else {
    print("O usuário está inativo.");
  }
  
  String nome = "Thiago";
  
  if (nome == "Thiago") {
    print("Olá Thiago!");
  } else {
    print("Não sei quem é você.");
  }
  
  double altura = 1.78;
  
  if (altura > 1.80) {
    print("Você é alto.");
  } else if (altura >= 1.70) {
    print("Você tem altura média.");
  } else {
    print("Você é baixo.");
  }
    
}